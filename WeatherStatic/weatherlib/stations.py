def _office(area: int) -> str:
    if area in _OFFICE_EXCEPTIONS:
        return _OFFICE_EXCEPTIONS[area]
    return f"{area // 1000 * 1000:06d}"


def _pref_code(area: int) -> int:
    """予報区域コードから都道府県コード（01〜47。北海道の予報区は 01 に丸める）。"""
    p = area // 10000
    return 1 if p == 0 or p < 2 else p  # 011000〜017010 → 01


